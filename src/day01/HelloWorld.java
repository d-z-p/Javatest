package day01;
public class HelloWorld{
	/* 这是我们主程序执行的入口
    	main方法
	*/
	public static void main(String[] args){
		// 定义变量 变量数据类型 变量名称=(赋值)
		int m=200;
 	    System.out.println(m);
 	    System.out.println(m);
 	    System.out.println(m);
 	    System.out.println(m);
		System.out.println("---------------------");
		// 将我们当前的变量值m=300
		m=300;
 	    System.out.println(m);
 	    System.out.println(m);
 	    System.out.println(m);
 	    System.out.println(m);
 	    System.out.println(m);
 	    System.out.println(m);		
	}

}
